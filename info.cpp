#include "back.h"
#include "sprite.h"
#include "texture.h"
#include "player.h"

//*****************************************************************************
// マクロ定義
//*****************************************************************************
#define TEX_WIDTH	(SCREEN_WIDTH)	//テクスチャの横幅
#define TEX_HEIGHT	(80.0f)	//テクスチャの縦幅

//*****************************************************************************
// プロトタイプ宣言
//*****************************************************************************


//*****************************************************************************
// グローバル変数
//*****************************************************************************
static int g_TexNo;

//=============================================================================
// 初期化処理
//=============================================================================
void InitInfo(void)
{
	//テクスチャの読み込み
	g_TexNo = LoadTexture((char*)"data/TEXTURE/info.png");
}

//=============================================================================
// 終了処理
//=============================================================================
void UninitInfo(void)
{

}

//=============================================================================
// 更新処理
//=============================================================================
void UpdateInfo(void)
{
}

//=============================================================================
// 描画処理
//=============================================================================
void DrawInfo(void)
{
	//線をなくす
	SetSamplerState(FILTER_MODE_POINT, ADDRESS_MODE_WRAP);
	
	//背景の描画
	DrawSpriteLeftTop(g_TexNo,
		0.0f, 1000.0f,
		TEX_WIDTH, TEX_HEIGHT,
		0.0f, 0.0f,
		1.0f, 1.0f);
}