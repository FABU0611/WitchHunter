static D3DXVECTOR2 g_CameraPos;	//カメラの座標
static D3DXVECTOR2 g_BasePos;	//ベースの座標

//=============================================================================
// 初期化処理
//=============================================================================
void InitCamera(void)
{
	g_CameraPos.x = 0.0f;
	g_CameraPos.y = 0.0f;
	g_BasePos.x = 0.0f;
	g_BasePos.y = 0.0f;
}

//=============================================================================
// 終了処理
//=============================================================================
void UninitCamera(void)
{

}

//=============================================================================
// 更新処理
//=============================================================================
void UpdateCamera(void)
{
}

//=============================================================================
// 描画処理
//=============================================================================
void DrawCamera(void)
{
}

void SetCameraPos(D3DXVECTOR2 pos)
{
	g_CameraPos = pos;
	//カメラ座標からベース座標を計算する
	g_BasePos = (g_CameraPos * -1);
}

D3DXVECTOR2 GetBasePos(void)
{
	return g_BasePos;
}